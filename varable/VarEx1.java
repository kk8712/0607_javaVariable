package org.java.varable;

public class VarEx1 {
	public static void main(String[] args) {
		System.out.println("변수의 데이터 타입");

		int a; // 데이터 타입이 int(정수형4byte)형인 변수 a를 선언했다.
		a = 1000; // 변수 a를 1000으로 초기화 했다.
		int a2 = 100; // 변수를 선언하고 바로 100으로 초기화
						// 할당 (메모리(저장공간)에 변수)
		int A;
		A = 10;
		System.out.println(a + A +a2);
		
		

	}
}